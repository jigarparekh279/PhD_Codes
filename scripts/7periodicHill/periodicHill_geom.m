function [X,Y] = periodicHill_geom(H, Nx)

X = linspace(0,9,Nx)*H;
y = X*0;

for i=1:Nx
    x = X(i);
    
    if (x > 198) 
        x = 252 - x;
    end
    
    if (x >= 0 && x < 9)
        y(i) = 28 + 6.775070969851E-03*x*x - 2.124527775800E-03*x*x*x;
    elseif (x >= 9 && x < 14)
        y(i) = 25.07355893131 + 0.9754803562315*x - 1.016116352781E-01*x*x + 1.889794677828E-03*x*x*x;
    elseif (x >= 14 && x < 20)
        y(i) = 2.579601052357E+01 + 8.206693007457E-01*x - 9.055370274339E-02*x*x + 1.626510569859E-03*x*x*x;
    elseif (x >= 20 && x < 30)
        y(i) = 4.046435022819E+01 - 1.379581654948E+00*x + 1.945884504128E-02*x*x - 2.070318932190E-04*x*x*x;
    elseif (x >= 30 && x < 40)
        y(i) = 1.792461334664E+01 + 8.743920332081E-01*x - 5.567361123058E-02*x*x + 6.277731764683E-04*x*x*x;
    elseif (x >= 40 && x < 54)
        y(i) = max(0,5.639011190988E+01- 2.010520359035E+00*x+ 1.644919857549E-02*x*x+ 2.674976141766E-05*x*x*x);
    end
    
end       

Y = y;

end